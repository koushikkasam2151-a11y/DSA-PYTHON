print("--- The Even/Odd Sorter ---")

# 1. Loop from 1 to 10
for i in range(1, 11):
    
    # 2. Logic Check: Is the number divisible by 2?
    if i % 2 == 0:
        print(f"Number {i} is EVEN")
    else:
        print(f"Number {i} is ODD")

print("--- Logic Check Complete ---")